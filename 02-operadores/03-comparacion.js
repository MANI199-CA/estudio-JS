//operadores de comparacion < menor que, >mayor que, === igual que, =< igual o menor que, 
// => igual o mayor que, != distinto a, !== distinto tipo de dato 
// cada ves que se usan los operadores de comparacion devuelve un boolean

let a = 10; 

// operadores de comparacion reglacionales
console.log(a > 5); //true
console.log(a < 5); // false
console.log(a >= 5); // true
console.log(a <= 5); // false

console.log(a === 5); //false
console.log(a != 5); // true
//mejor usar estos 
console.log(a !== '10');
console.log(a === '10');


// METODOS PARA AGREGAR Y ELIMINAR ELEMENTOS

//1- push() : agrega uno o mas elementos al final de un array y devuelve un nuevo array actualizado con nueva longitud
//2- pop() : elimina el ultimo elemneto del array y lo devuelve actualizado
//3- unshift() : agrega uno o mas elementos al inicio del array y lo devuelve actualizado
//4- shift() : elimina el primer elemento de un array y devuelve el array actualizado

// METODOS PARA MODIFICAR EL CONTENIDO DE UN ARRAY 

//1- splice() : cambia el contenido de un array eliminando, reemplazando o agregando elementos
//2- fill() : rellena todos los elementos de un array con un valor especifico
//3- reverse() : invierte el orden de los elementos de un array

// METODOS  PARA BUSCAR Y FILTRAR ELEMENTOS

//1- indexOf() : devuelve el primer indice en el que se encuentra un elemento en 
//               especifico, o -1 si no se encuentra.
//2- lastIndexOf() : decuelve el ultimo elemento en el que se encuentra un elemento en especifico, 
//                   y si no lo encuentra devuelce -1.
//3- find() : devuelve el primer elemento en el array que cumpla con una condicion especifica
//4- filter() : CREA UN NUEVO ARRAY CON TODOS LOS ELEMENTOS QUE CUMPLAN CON UNA CONDICION ESPECIFICA


// METODOS PARA TRANSFORMAR Y ORDENAR

//1- map() : crea un nuevo array con los resultados de aplicar una funcion a cada elemntos de un array
//2- sort() : ordena los elemntos de un array alfabeticamente o segun una funcion de comparacion
//3- concat() : combina dos o mas array y deuelve un nuevo array resultante 
//4- slice() : devuelve una copia superficial de una porcion del array en un nuevo array

// METODOS PARA OBTENER INFORMACION DE UN ARRAY 

//1- length : propiedad que deculeve la cantidad de elemntos de un array 
//2- includes() : comprueba si un elemnto esta presente en un array o si lo incluye
//3- every() : verifica si todos los elemntos cumplen con una condicion especifica
//4- some() : verifica si al menpos un elemnto cumple con una condicion especifica

